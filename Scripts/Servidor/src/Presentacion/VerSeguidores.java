    private void jTextFieldSeguidorKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldSeguidorKeyReleased

        DefaultTableModel model = (DefaultTableModel) jTableSeguidor.getModel();

        model.setRowCount(0);

        for (int i = 0; i < jTableSeguidor.getRowCount(); i++) {
            model.removeRow(i);
            i -= 1;
        }

        model = (DefaultTableModel) jTableSeguidor.getModel();
        model.setRowCount(0);

        List<DtSeguidor> listU = Fabrica.getInstance().getIControladorUsuario().MostrarUsuarios();
        for (int i = 0; i < listU.size(); i++) {
            DtSeguidor uSeg = (DtSeguidor) listU.get(i);
            if ((!jTextFieldSeguidor.getText().isEmpty()) && uSeg.getNickName().contains(jTextFieldSeguidor.getText())) {
                Object[] dat = {uSeg.getNickName(), uSeg.getNombre(), uSeg.getApellido(), uSeg.getTipo()};
                model.addRow(dat);
            } else if (jTextFieldSeguidor.getText().isEmpty()) {
                Object[] dat = {uSeg.getNickName(), uSeg.getNombre(), uSeg.getApellido(), uSeg.getTipo()};
                model.addRow(dat);
            }
        }


    }//GEN-LAST:event_jTextFieldSeguidorKeyReleased

